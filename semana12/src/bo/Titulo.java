package bo;

public class Titulo {
	private String id_titulo;
	private String nome;
	private String genero;
	private String qtd_copias;
	
	public String getId_titulo() {
		return id_titulo;
	}
	public void setId_titulo(String id_titulo) {
		this.id_titulo = id_titulo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getGenero() {
		return genero;
	}
	public void setGenero(String genero) {
		this.genero = genero;
	}
	public String getQtd_copias() {
		return qtd_copias;
	}
	public void setQtd_copias(String qtd_copias) {
		this.qtd_copias = qtd_copias;
	}
}
